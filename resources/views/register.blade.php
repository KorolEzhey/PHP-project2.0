  <h1>Рецепт Домашней Пиццы</h1>

  <h2>Ингредиенты:</h2>

  <h3>Для теста:</h3>
  <p>
    2,5 чашки муки<br>
    1 чайная ложка сахара<br>
    1 чайная ложка соли<br>
    1 чайная ложка сухих дрожжей<br>
    1 чашка теплой воды<br>
    2 столовые ложки оливкового масла
  </p>

  <h3>Для начинки:</h3>
  <p>
    1/2 чашки томатного соуса<br>
    1 чайная ложка итальянских трав (смесь базилика, орегано, розмарина)<br>
    2 чашки тертого моцарелла сыра
  </p>

  <p>Дополнительные ингредиенты на ваш выбор: нарезанные помидоры, шампиньоны, перчики, пепперони и т.д.</p>

  <h2>Инструкции:</h2>

  <h3>Приготовление теста:</h3>
  <ol>
    <li>В миске смешайте муку, сахар, соль и сухие дрожжи.</li>
    <li>Постепенно добавляйте теплую воду и оливковое масло, перемешивая, пока тесто не станет мягким и эластичным.</li>
    <li>Выложите тесто на легко помученную поверхность и продолжайте месить в течение 5-7 минут, пока оно не станет гладким.</li>
    <li>Положите тесто в миску, прикройте его чистым полотенцем и оставьте подниматься в теплом месте около 1 часа или до удвоения размера.</li>
  </ol>

  <h3>Сборка пиццы:</h3>
  <ol>
    <li>Разогрейте духовку до 220 градусов Цельсия.</li>
    <li>На легко помученной поверхности раскатайте тесто в круглую форму (или другую форму на ваш выбор).</li>
    <li>Перенесите тесто на противень, предварительно покрытый бумагой для выпечки.</li>
    <li>Разогрейте томатный соус и равномерно распределите его по тесту, оставляя края свободными.</li>
    <li>Посыпьте итальянскими травами по соусу.</li>
    <li>Посыпьте тертым моцарелла сыром и добавьте любимые ингредиенты.</li>
    <li>Печьте в разогретой духовке 12-15 минут или до золотистой корки и плавления сыра.</li>
    <li>Выньте из духовки, нарежьте на порции и подавайте.</li>
  </ol>

  <p>Приятного аппетита!</p>

